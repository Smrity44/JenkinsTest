package ControlFlowStatements._04_Loops.While;
//In the below example, we print integer values from 1 to 10. Unlike the for loop,

public class _o3_DoWhileExample101 {
    public static void main(String[] args) {
        int i=1;
        do{
            System.out.println(i);
            i++;
        }while(i<=10);
    }
}
